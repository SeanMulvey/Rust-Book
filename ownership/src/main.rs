fn main() {

    // String push and pop
    let mut x = String::from("hello");
    println!("{x}");
    // Append "world!" to x
    x.push_str(" world!");
    println!("{x}");
    // Pop the last 7 characters from x ("world!")
    x.pop();
    println!("{x}");
    x.pop();
    println!("{x}");
    x.pop();
    println!("{x}");
    x.pop();
    println!("{x}");
    x.pop();
    println!("{x}");
    x.pop();
    println!("{x}");
    x.pop();
    println!("{x}");

}
